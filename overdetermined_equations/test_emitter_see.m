% 给定参数（使用新约定：法线朝左为正）
x0 = 4;          % 接收器x坐标
y0 = 2;          % 接收器y坐标
alpha = 0;       % 接收器朝向角度(弧度)：法线朝左为正，这里0表示法线朝左
phi1 = pi/8;    % 第一个到达角(15度)
phi2 = -pi/8;     % 第二个到达角(22.5度)
D = 0.7654;         % sinθ1 - sinθ2
K = 1.656;         % 路径长度差

% 设置权重
weights = [1, 1, 0.5, 0.5];

% 求解发射器位置和方向
[x, y, beta] = solve_emitter_sse(x0, y0, alpha, phi1, phi2, D, K, weights);

% 显示结果
fprintf('发射器位置: (%.4f, %.4f)\n', x, y);
fprintf('发射器朝向: %.4f 弧度 (法线朝左为正)\n', beta);